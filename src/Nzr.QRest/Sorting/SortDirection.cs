namespace Nzr.QRest.Sorting;

/// <summary>
/// Represents the direction of sorting in a query.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Sorts the results in ascending order (e.g., from lowest to highest, A-Z).
    /// </summary>
    ASC,

    /// <summary>
    /// Sorts the results in descending order (e.g., from highest to lowest, Z-A).
    /// </summary>
    DESC
}
